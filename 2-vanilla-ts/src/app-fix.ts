function add(num1: number, num2: number) {
  return num1 + num2;
}
function minus(num1: number, num2: number) {
  return num1 - num2;
}
function multiple(num1: number, num2: number) {
  return num1 * num2;
}
function divide(num1: number, num2: number) {
  return num1 / num2;
}
function mod(num1: number, num2: number) {
  return num1 % num2;
}

add(1, 2);
minus(1, 2);
multiple(1, 2);
divide(1, 2);
mod(1, 2);
